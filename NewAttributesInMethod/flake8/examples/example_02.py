class Weapon:
    def __init__(attack):
        self.attack = attack

class Cratos:
    def __init__(self,weapon):
        self.weapon = weapon
    def set_weapon(weapon):
        self.weapon = weapon
    def hit(enemy):
        enemy.life = enemy.life - self.weapon

class Drauf:
    def __init__(self,life):
        self.life = life
    def get_hit(damage):
        self.damage=damage
        self.life= self.life-damage
